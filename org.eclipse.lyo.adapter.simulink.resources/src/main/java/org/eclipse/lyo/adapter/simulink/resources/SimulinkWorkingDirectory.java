@OslcNamespace(Constants.SIMULINK_NAMESPACE)
@OslcName("WorkingDirectory")
@OslcResourceShape(title = "WorkingDirectory Resource Shape", describes = Constants.TYPE_SIMULINK_WORKINGDIRECTORY)
public class SimulinkWorkingDirectory extends AbstractResource{

	public SimulinkWorkingDirectory() throws URISyntaxException {
		super();
	}
	public SimulinkWorkingDirectory(URI about) throws URISyntaxException {
		super(about);
	}

	// ********* model *********
	private final Set<Link> models = new HashSet<Link>();

	public void setModels(final Link[] models) {
		this.models.clear();
		if (models != null)
		{
			this.models.addAll(Arrays.asList(models));
		}
	}

	@OslcDescription("Description of WorkingDirectory::model TBD")
	@OslcName("model")
	@OslcOccurs(Occurs.ZeroOrMany)
	@OslcPropertyDefinition("http://mathworks.com/simulink/rdf#WorkingDirectory/model")
	@OslcTitle("model")
	@OslcReadOnly(false)
	public Link[]  getModels() {
		 return models.toArray(new Link[models.size()]);
	}

	private URI      serviceProvider;

	public void setServiceProvider(final URI serviceProvider)
	{		this.serviceProvider = serviceProvider;
	}

	@OslcDescription("The scope of a resource is a URI for the resource's OSLC Service Provider.")
	@OslcPropertyDefinition(OslcConstants.OSLC_CORE_NAMESPACE + "serviceProvider")
	@OslcRange(OslcConstants.TYPE_SERVICE_PROVIDER)
	@OslcTitle("Service Provider")	
	public URI getServiceProvider()
	{
		return serviceProvider;
	}

}